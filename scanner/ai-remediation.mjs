/**
 * AI-powered remediation suggestion generator.
 *
 * Uses the GitHub Models API (OpenAI-compatible) to generate before/after code
 * fixes for the most common accessibility violations found during a scan.
 *
 * Design principles:
 * - Only triggered when the REMEDIATE keyword is present in the issue title
 *   (opt-in to control API costs)
 * - Deduplicates by rule: one API call per unique violation type, regardless of
 *   how many times it appears across scanned pages
 * - Processes only the top 10 violations by occurrence count
 * - Gracefully degrades when the GitHub Models API is unavailable
 * - Includes an AI disclaimer in all generated output
 */

const GITHUB_MODELS_ENDPOINT =
  "https://models.inference.ai.azure.com/chat/completions";

/** Maximum number of distinct violation types to generate suggestions for. */
const MAX_SUGGESTIONS = 10;

/** Model used for suggestions — gpt-4o-mini balances quality and cost. */
const DEFAULT_MODEL = "gpt-4o-mini";

/**
 * Disclaimer text appended to all AI-generated output.
 * @type {string}
 */
export const AI_DISCLAIMER =
  "⚠️ **AI Disclaimer**: These suggestions are generated by an AI model " +
  "(GitHub Models / gpt-4o-mini) and may contain errors or omissions. " +
  "Always review and test suggested fixes in your own codebase before applying them. " +
  "Suggestions are based on the example HTML snippets captured during scanning and " +
  "may not account for your full page context.";

/**
 * Build the system prompt for the AI model.
 * @returns {string}
 */
function buildSystemPrompt() {
  return (
    "You are an expert web accessibility engineer specialising in WCAG 2.2 compliance. " +
    "When given details of an accessibility violation, you provide a concise, " +
    "actionable code fix with a brief explanation. " +
    "Always respond with valid JSON only — no markdown fences, no prose outside the JSON object."
  );
}

/**
 * Build the user prompt for a single violation type.
 *
 * @param {object} violation - A consolidated failure entry from buildEnhancedSummary().
 * @returns {string}
 */
function buildUserPrompt(violation) {
  const { rule, engine, metadata, wcag, examples } = violation;
  const description =
    violation.ruleTitle || metadata?.description || rule;
  const wcagScs =
    wcag?.scs?.length > 0
      ? `${wcag.scs.join(", ")} (Level ${wcag.level || "?"})`
      : "not mapped";
  const exampleHtml =
    examples?.[0]?.html ? examples[0].html.slice(0, 500) : "(no example available)";
  const exampleMessage =
    examples?.[0]?.message ? examples[0].message.slice(0, 300) : "";

  return (
    `Accessibility violation details:\n` +
    `- Rule ID: ${rule}\n` +
    `- Engine: ${engine}\n` +
    `- Description: ${description}\n` +
    `- WCAG Success Criteria: ${wcagScs}\n` +
    `- Example failing HTML: ${exampleHtml}\n` +
    (exampleMessage ? `- Failure message: ${exampleMessage}\n` : "") +
    `\n` +
    `Respond with a JSON object containing exactly these fields:\n` +
    `{\n` +
    `  "beforeCode": "<the problematic element or pattern, similar to the example>",\n` +
    `  "afterCode": "<the corrected element with the fix applied>",\n` +
    `  "explanation": "<one or two sentences explaining why this is an issue and what the fix does>",\n` +
    `  "wcagReference": "<WCAG SC number and short criterion name, e.g. 1.4.3 Contrast (Minimum)>"\n` +
    `}`
  );
}

/**
 * Call the GitHub Models API for a single violation.
 *
 * @param {object} violation - A consolidated failure entry.
 * @param {string} token - GitHub personal access token with models:read scope.
 * @param {string} model - Model identifier.
 * @returns {Promise<{beforeCode: string, afterCode: string, explanation: string, wcagReference: string}|null>}
 */
async function fetchSuggestionFromApi(violation, token, model) {
  const body = JSON.stringify({
    model,
    messages: [
      { role: "system", content: buildSystemPrompt() },
      { role: "user", content: buildUserPrompt(violation) }
    ],
    temperature: 0.2,
    max_tokens: 512
  });

  const response = await fetch(GITHUB_MODELS_ENDPOINT, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${token}`
    },
    body,
    signal: AbortSignal.timeout(30000)
  });

  if (!response.ok) {
    const text = await response.text().catch(() => "");
    throw new Error(
      `GitHub Models API returned ${response.status}: ${text.slice(0, 200)}`
    );
  }

  const data = await response.json();
  const content = data?.choices?.[0]?.message?.content ?? "";

  // Strip optional markdown code fences the model sometimes adds
  const cleaned = content.replace(/^```(?:json)?\s*/i, "").replace(/\s*```\s*$/, "").trim();

  const parsed = JSON.parse(cleaned);
  return {
    beforeCode: String(parsed.beforeCode ?? ""),
    afterCode: String(parsed.afterCode ?? ""),
    explanation: String(parsed.explanation ?? ""),
    wcagReference: String(parsed.wcagReference ?? "")
  };
}

/**
 * Generate AI-powered remediation suggestions for the top violations.
 *
 * Violations are expected to be pre-sorted by occurrence count (descending),
 * as returned by buildEnhancedSummary(). Only the first MAX_SUGGESTIONS unique
 * rule types are processed — one API call per type, regardless of page count.
 *
 * @param {Array<object>} consolidatedFailures - Sorted array of consolidated failures.
 * @param {object} options
 * @param {string} options.token - GitHub token (GITHUB_TOKEN env var).
 * @param {string} [options.model] - Model ID override (default: gpt-4o-mini).
 * @returns {Promise<{suggestions: Array<object>, disclaimer: string, model: string}>}
 */
export async function generateRemediationSuggestions(
  consolidatedFailures,
  { token, model = DEFAULT_MODEL } = {}
) {
  if (!token) {
    console.error(
      "[ai-remediation] GITHUB_TOKEN not available — skipping AI suggestions"
    );
    return { suggestions: [], disclaimer: AI_DISCLAIMER, model };
  }

  if (!Array.isArray(consolidatedFailures) || consolidatedFailures.length === 0) {
    return { suggestions: [], disclaimer: AI_DISCLAIMER, model };
  }

  // Take only the top N by occurrence count (already sorted)
  const topViolations = consolidatedFailures.slice(0, MAX_SUGGESTIONS);

  const suggestions = [];

  for (const violation of topViolations) {
    const ruleKey = `${violation.engine}:${violation.rule}`;
    console.error(
      `[ai-remediation] Requesting suggestion for ${ruleKey} ` +
        `(${violation.totalOccurrences} occurrences across ${violation.pages.size ?? violation.pages?.size ?? 0} pages)`
    );

    let suggestion = null;
    try {
      suggestion = await fetchSuggestionFromApi(violation, token, model);
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      console.error(
        `[ai-remediation] Failed to get suggestion for ${ruleKey}: ${msg}`
      );
      // Continue with null — we'll include a placeholder so the caller knows
      // the rule was processed even if the API failed
    }

    suggestions.push({
      ruleKey,
      rule: violation.rule,
      engine: violation.engine,
      ruleTitle: violation.ruleTitle || violation.metadata?.description || violation.rule,
      ruleUrl: violation.ruleUrl || null,
      wcag: violation.wcag,
      totalOccurrences: violation.totalOccurrences,
      pageCount: violation.pages instanceof Map
        ? violation.pages.size
        : (violation.pages?.length ?? 0),
      suggestion
    });
  }

  return { suggestions, disclaimer: AI_DISCLAIMER, model };
}

/**
 * Format AI remediation suggestions as a Markdown section.
 *
 * @param {{suggestions: Array<object>, disclaimer: string, model: string}} remediationResult
 * @returns {string} Markdown text
 */
export function formatRemediationMarkdown(remediationResult) {
  const { suggestions, disclaimer, model } = remediationResult;

  const lines = [];
  lines.push("## 🤖 AI-Powered Remediation Suggestions");
  lines.push("");
  lines.push(
    `> ${disclaimer}`
  );
  lines.push("");

  if (suggestions.length === 0) {
    lines.push(
      "*No suggestions could be generated. The GitHub Models API may be unavailable " +
        "or no violations were detected.*"
    );
    lines.push("");
    return lines.join("\n");
  }

  lines.push(
    `The following suggestions were generated by **${model}** for the top ` +
      `${suggestions.length} most common violation type${suggestions.length !== 1 ? "s" : ""}.`
  );
  lines.push("");

  for (let i = 0; i < suggestions.length; i++) {
    const item = suggestions[i];
    const title = item.ruleTitle || item.rule;
    const ruleDisplay = item.ruleUrl
      ? `[${item.rule}](${item.ruleUrl})`
      : item.rule;

    lines.push(
      `### ${i + 1}. ${title} (${item.engine.toUpperCase()} rule: ${ruleDisplay})`
    );
    lines.push("");
    lines.push(
      `**Occurrences:** ${item.totalOccurrences} across ${item.pageCount} page${item.pageCount !== 1 ? "s" : ""}`
    );
    if (item.wcag?.scs?.length > 0) {
      const levelBadge = item.wcag.level ? ` — Level ${item.wcag.level}` : "";
      lines.push(`**WCAG:** ${item.wcag.scs.join(", ")}${levelBadge}`);
    }
    lines.push("");

    if (!item.suggestion) {
      lines.push("*Suggestion unavailable — API call failed for this rule.*");
      lines.push("");
      continue;
    }

    const { beforeCode, afterCode, explanation, wcagReference } = item.suggestion;

    if (beforeCode) {
      lines.push("**Before (failing):**");
      lines.push("```html");
      lines.push(beforeCode);
      lines.push("```");
      lines.push("");
    }

    if (afterCode) {
      lines.push("**After (fixed):**");
      lines.push("```html");
      lines.push(afterCode);
      lines.push("```");
      lines.push("");
    }

    if (explanation) {
      lines.push(`💡 **Why this matters:** ${explanation}`);
      lines.push("");
    }

    if (wcagReference) {
      lines.push(`📖 **WCAG Reference:** ${wcagReference}`);
      lines.push("");
    }

    lines.push("---");
    lines.push("");
  }

  return lines.join("\n");
}

// Only run as a CLI tool when executed directly
if (import.meta.url === `file://${process.argv[1]}`) {
  console.error(
    "[ai-remediation] This module is not designed to run directly. " +
      "Import generateRemediationSuggestions from run-scan.mjs."
  );
  process.exit(1);
}
